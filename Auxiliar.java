/**
 * clase auxiliar que prové dos métodos
 * para calcular el valor doble y triple de un número
 * @author Manuel Molino
 * @version 1.0.0
 */
 public class Auxiliar {
	
	/**
	 * constante para ejemplo
	 */
	public static final int CONSTANTE = 5;
	
	/**Método que devuelve el valor doble
	 * @param valor del número que queremos obtener el valor doble
	 * @return el valor doble del número
	 */
	public static int obtenerValorDuplicado (int valor) {
		return valor * 2;
	}
	
	/**Método que devuelve el valor triple
	 * @param valor del número que queremos obtener el valor triple
	 * @return el valor triple del número
	 */
	public static int obtenerValorTriple (int valor) {
		return valor * 3;
	}
}

